import os, json, base64, urllib.request, urllib.error
from dotenv import load_dotenv
load_dotenv()

BASE_URL  = os.environ.get('CONFLUENCE_BASE_URL', '').rstrip('/')
EMAIL     = os.environ.get('JIRA_EMAIL', '')
TOKEN     = os.environ.get('JIRA_API_TOKEN', '')
SPACE_KEY = os.environ.get('CONFLUENCE_SPACE_KEY', 'HR')


def _auth():
    t = base64.b64encode(f'{EMAIL}:{TOKEN}'.encode()).decode()
    return {'Authorization': f'Basic {t}', 'Content-Type': 'application/json'}


def _req(method, path, payload=None):
    url  = f'{BASE_URL}/rest/api/{path}'
    data = json.dumps(payload).encode('utf-8') if payload else None
    req  = urllib.request.Request(url, data=data, headers=_auth(), method=method)
    try:
        with urllib.request.urlopen(req, timeout=15) as r:
            return json.loads(r.read()), None
    except urllib.error.HTTPError as e:
        body = e.read().decode('utf-8', errors='ignore')
        return None, f'HTTP {e.code}: {body[:300]}'


def create_page(title, body_html, parent_id=None):
    payload = {
        'type':  'page',
        'title': title,
        'space': {'key': SPACE_KEY},
        'body':  {'storage': {'value': body_html, 'representation': 'storage'}},
    }
    if parent_id:
        payload['ancestors'] = [{'id': parent_id}]
    result, err = _req('POST', 'content', payload)
    if err:
        print(f'  ✗ [{title}] {err}')
        return None
    page_id = result.get('id')
    url     = result.get('_links', {}).get('webui', '')
    print(f'  ✓ [{title}] id={page_id} {url}')
    return page_id


def get_or_create_root(title):
    """최상위 페이지 조회 또는 생성"""
    result, _ = _req('GET', f'content?spaceKey={SPACE_KEY}&title={urllib.parse.quote(title)}&type=page')
    if result and result.get('results'):
        pid = result['results'][0]['id']
        print(f'  ~ [{title}] 이미 존재 id={pid}')
        return pid
    return create_page(title, f'<p>TalentCore 사내 위키 — {title}</p>')


